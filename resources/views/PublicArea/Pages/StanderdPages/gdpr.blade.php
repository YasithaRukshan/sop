            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;2.5 Lawfulness
                of Processing</h3>
            <p>There are
                six alternative ways in which the lawfulness of a specific case of processing of personal data may be
                established under the GDPR. It is the Company&rsquo;s policy to identify the appropriate basis for
                processing and to document it, in accordance with the Regulation. The options are described in brief in
                the following sections.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;2.5.1 Consent</h3>
            <p>Unless it is
                necessary for a reason allowable in the GDPR, the Company will always obtain explicit consent from a
                data subject to collect and process their data. In case of children below the age of 16 (a lower age may
                be allowable in specific EU member states) parental consent will be obtained. Transparent information
                about our usage of their personal data will be provided to data subjects at the time that consent is
                obtained and their rights with regard to their data explained, such as the right to withdraw consent.
                This information will be provided in an accessible form, written in clear language and free of charge.
                If the personal data are not obtained directly from the data subject then this information will be
                provided to the data subject within a reasonable period after the data are obtained and definitely
                within one month.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.5.2 Performance of a Contrac</h3>
            <p>Where the
                personal data collected and processed are required to fulfil a contract with the data subject, explicit
                consent is not required. This will often be the case where the contract cannot be completed without the
                personal data in question e.g. a delivery cannot be made without an address to deliver to.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.5.3 Legal
                Obligation</h3>
            <p>If the
                personal data is required to be collected and processed in order to comply with the law, then explicit
                consent is not required. This may be the case for some data related to employment and taxation for
                example, and for many areas addressed by the public sector.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.5.4 Vital
                Interests of the Data Subject</h3>
            <p>In a case
                where the personal data are required to protect the vital interests of the data subject or of another
                natural person, then this may be used as the lawful basis of the processing. The Company will retain
                reasonable, documented evidence that this is the case, whenever this reason is used as the lawful basis
                of the processing of personal data. As an example, this may be used in aspects of social care,
                particularly in the public sector.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.5.5 Task
                Carried Out in the Public Interest</h3>
            <p>Where the
                Company needs to perform a task that it believes is in the public interest or as part of an official
                duty then the data subject&rsquo;s consent will not be requested. The assessment of the public interest
                or official duty will be documented and made available as evidence where required.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.5.6 Legitimate Interests</h3>
            <p>If the
                processing of specific personal data is in the legitimate interests of the Company and is judged not to
                affect the rights and freedoms of the data subject in a significant way, then this may be defined as the
                lawful reason for the processing. Again, the reasoning behind this view will be documented.</p>
            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.6 Privacy
                by Design</h3>
            <p>The Company
                has adopted the principle of privacy by design and will ensure that the definition and planning of all
                new or significantly changed systems that collect or process personal data will be subject to due
                consideration of privacy issues, including the completion of one or more data protection impact
                assessments.</p>
            <p>The data
                protection impact assessment will include:</p>
            <p>
                 &bull;&nbsp;&nbsp;Consideration of how personal data will be processed and for what purposes</p>
            <p>
                 &bull;&nbsp;&nbsp;Assessment of whether the proposed processing of personal data is both necessary and proportionate
                to the purpose(s)</p>
            <p>
                 &bull;&nbsp;&nbsp;Assessment of the risks to individuals in processing the personal data</p>
            <p> &bull;&nbsp;&nbsp;What
                controls are necessary to address the identified risks and demonstrate compliance with legislation</p>
            <p>Use of
                techniques such as data minimization and pseudonymization will be considered where applicable and
                appropriate.</p>
            <p><br></p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.7 Contracts
                Involving the Processing of Personal Data</h3>
            <p>The Company
                will ensure that all relationships it enters into that involve the processing of personal data are
                subject to a documented contract that includes the specific information and terms required by the GDPR.
                For more information, see the GDPR Controller-Processor Agreement Policy.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.8 International Transfers of Personal Data</h3>
            <p>Transfers of
                personal data outside the European Union will be carefully reviewed prior to the transfer taking place
                to ensure that they fall within the limits imposed by the GDPR. This depends partly on the European
                Commission&rsquo;s judgement as to the adequacy of the safeguards for personal data applicable in the
                receiving country and this may change over time. Intra-group international data transfers will be
                subject to legally binding agreements referred to as Binding Corporate Rules (BCR) which provide
                enforceable rights for data subjects.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.9 Data
                Protection Officer</h3>
            <p>A defined
                role of Data Protection Officer (DPO) is required under the GDPR if an organization is a public
                authority, if it performs large scale monitoring or if it processes particularly sensitive types of data
                on a large scale. The DPO is required to have an appropriate level of knowledge and can either be an in-
                house resource or outsourced to an appropriate service provider. Based on these criteria, the Company
                does not require a Data Protection Officer to be appointed.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.10 Breach
                Notification</h3>
            <p>It is the
                Company&rsquo;s policy to be fair and proportionate when considering the actions to be taken to inform
                affected parties regarding breaches of personal data. In line with the GDPR, where a breach is known to
                have occurred which is likely to result in a risk to the rights and freedoms of individuals, the
                relevant supervisory authority will be informed within 72 hours. This will be managed in accordance with
                our Information Security Incident Response Procedure which sets out the overall process of handling
                information security incidents. Under the GDPR the relevant DPA has the authority to impose a range of
                fines of up to four percent of annual worldwide turnover or twenty million Euros, whichever is the
                higher, for infringements of the regulations.</p>

            <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp; 2.11 Addressing
                Compliance to the GDPR</h3>
            <p>The
                following actions are undertaken to ensure that the Company always complies with the accountability
                principle of the GDPR:</p>
            <p> &bull;&nbsp;&nbsp;The
                legal basis for processing personal data is clear and unambiguous</p>
            <p> &bull;&nbsp;&nbsp;A Data
                Protection Officer is appointed with specific responsibility for data protection in the organization (if
                required)</p>
            <p> &bull;&nbsp;&nbsp;All
                staff involved in handling personal data understand their responsibilities for following good data
                protection practice</p>
            <p>
                 &bull;&nbsp;&nbsp;Training in data protection has been provided to all staff</p>
            <p> &bull;&nbsp;&nbsp;Rules
                regarding consent are followed</p>
            <p> &bull;&nbsp;&nbsp;Routes
                are available to data subjects wishing to exercise their rights regarding personal data and such
                enquiries are handled effectively</p>
            <p>
                 &bull;&nbsp;&nbsp;Regular reviews of procedures involving personal data are carried out</p>
            <p>
                 &bull;&nbsp;&nbsp;Privacy by design is adopted for all new or changed systems and processes</p>
            <p> &bull;&nbsp;&nbsp;The
                following documentation of processing activities is recorded:</p>
            <p>
                 &bull;&nbsp;&nbsp;TOrganization name and relevant details</p>
            <p> &bull;&nbsp;&nbsp;TPurposes of
                the personal data processing</p>
            <p> &bull;&nbsp;&nbsp;TCategories
                of individuals and personal data processed</p>
            <p> &bull;&nbsp;&nbsp;TCategories
                of personal data recipients</p>
            <p> &bull;&nbsp;&nbsp;TAgreements and
                mechanisms for transfers of personal data to non-EU countries including details of controls in place</p>
            <p> &bull;&nbsp;&nbsp;TPersonal data
                retention schedules</p>
            <p> &bull;&nbsp;&nbsp;TRelevant
                technical and organizational controls in place</p>
            <p>These actions
                are reviewed on a regular basis as part of the management process concerned with data protection.</p>
