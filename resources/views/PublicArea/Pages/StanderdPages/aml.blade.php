                foreign bank) for foreign shell banks. Upon finding or suspecting such accounts, Company employees
                will
                notify the AML Compliance Person, who will terminate any verified correspondent account in the
                United
                States for a foreign shell bank. We will also terminate any correspondent account that we have
                determined is not maintained by a foreign shell bank but is being used to provide services to such a
                shell bank. We will exercise caution<strong>&nbsp;</strong>regarding liquidating positions in such
                accounts and take reasonable steps to ensure that no new positions are established in these accounts
                during the termination period. We will terminate any correspondent account for which we have not
                obtained the information described in Appendix A of the regulations regarding shell banks within the
                time periods specified in those regulations.</p>
