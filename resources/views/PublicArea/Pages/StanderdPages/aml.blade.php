            </ul>
            <p><br></p>
            <ul class="ml-4">
                <li>
                    <p>
                         &bull;&nbsp;&nbsp;Obtaining a financial statement.</p>
                </li>
            </ul>
            <p>
                <br></p>
            <p>
                We will use non-documentary methods of verification when:</p>
            <p>
                (1) the customer is unable to present an unexpired government-issued identification document with a
                photograph or other similar safeguard;</p>
            <p>
                (2) the Company is unfamiliar with the documents the customer presents for identification
