            <h3>
                &nbsp;&nbsp;&nbsp;&nbsp;a.Detecting and Closing Correspondent Accounts of Foreign Shell Banks</h3>
            <p>
                We will identify foreign bank accounts and any such account that is a correspondent account (any
                account
                that is established for a foreign bank to receive deposits from, or to make payments or other
                disbursements on behalf of, the foreign bank, or to handle other financial transactions related to
