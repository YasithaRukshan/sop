            <h3 class="row clearfix justify-content-center">ANTI MONEY LAUNDERING PROGRAM</h3>
        <h3>
            1.&nbsp;&nbsp;Company Policy</h3>

        <p>
            It is the policy of Solar Oil Enterprises Limited (the &ldquo;Company&rdquo;) to prohibit and actively
            prevent money laundering and any activity that facilitates money laundering or the funding of terrorist
            or criminal activities by complying with all applicable requirements under the Bank Secrecy Act (BSA)
            and its implementing regulations.</p>
        <p>
            <br></p>
        <p>
            Money laundering is generally defined as engaging in acts designed to conceal or disguise the true
            origins of criminally derived proceeds so that the proceeds appear to have derived from legitimate
            origins or constitute legitimate assets. Generally, money laundering occurs in three stages. Cash first
            enters the financial system at the &quot;placement&quot; stage, where the cash generated from criminal
            activities is converted into monetary instruments, such as money orders or traveler&apos;s checks, or
            deposited into accounts at financial institutions. At the &quot;layering&quot; stage, the funds are
            transferred or moved into other accounts or other financial institutions to further separate the money
            from its criminal origin. At the &quot;integration&quot; stage, the funds are reintroduced into the
            economy and used to purchase legitimate assets or to fund other criminal activities or legitimate
            businesses.</p>
        <p>
            <br></p>
        <p>
            Terrorist financing may not involve the proceeds of criminal conduct, but rather an attempt to conceal
            either the origin of the funds or their intended use, which could be for criminal purposes. Legitimate
            sources of funds are a key difference between terrorist financiers and traditional criminal
            organizations. In addition to charitable donations, legitimate sources include foreign government
            sponsors, business ownership and personal employment. Although the motivation differs between
            traditional money launderers and terrorist financiers, the actual methods used to fund terrorist
            operations can be the same as or similar to methods used by other criminals to launder funds. Funding
            for terrorist attacks does not always require large sums of money and the associated transactions may
            not be complex.</p>
        <p>
            <br></p>
        <p>
            Our AML policies, procedures and internal controls are designed to ensure compliance with all applicable
            BSA regulations and will be reviewed and updated on a regular basis to ensure appropriate policies,
            procedures and internal controls are in place to account for both changes in regulations and changes in
            our business.</p>
        <p>
            <br></p>

        <h3>
            2.&nbsp;&nbsp;AML Compliance Person Designation and Duties</h3>
        <p>
            The Company has designated its Chief Compliance Officer as its Anti-Money Laundering Program Compliance
            Person (AML Compliance Person), with full responsibility for the Company&rsquo;s AML program. The AML
            Compliance Person has a working knowledge of the BSA and its implementing regulations and is qualified
            by experience, knowledge and training. The duties of the AML Compliance Person will include monitoring
            the Company&rsquo;s compliance with AML obligations, overseeing communication and training for
            employees<em>.&nbsp;</em>The AML Compliance Person will also ensure that the Company keeps and maintains
            all of the required AML records and will ensure that Suspicious Activity Reports (SAR-SFs) are filed
            with the Financial Crimes Enforcement Network (FinCEN) when appropriate. The AML Compliance Person is
            vested with full responsibility and authority to enforce the Company&rsquo;s AML program.</p>

        <h3>
            3.&nbsp;&nbsp;Giving AML Information to Federal Law Enforcement Agencies and Other Financial Institutions
        </h3>
        <h3>
            &nbsp;&nbsp;&nbsp;&nbsp;a.FinCEN Requests Under USA PATRIOT Act Section 314(a)</h3>
        <p>
            <br></p>
        <p>
            We will respond to a Financial Crimes Enforcement Network (FinCEN) request concerning accounts and
            transactions (a 314(a) Request) by immediately searching our records to determine whether we maintain or
            have maintained any account for, or have engaged in any transaction with, each individual, entity or
            organization named in the 314(a) Request as outlined in the Frequently Asked Questions (FAQ) located on
            FinCEN&rsquo;s secure Web site. We understand that we have 14 days (unless otherwise specified by
            FinCEN) from the transmission date of the request to respond to a 314(a) Request. Unless otherwise
            specified by FinCEN, we are required to search those documents outlined in FinCEN&rsquo;s FAQ. If we
            find a match, the AML Compliance Person will report it to FinCEN via FinCEN&rsquo;s Web-based 314(a)
            Secure Information Sharing System within 14 days or within the time requested by FinCEN in the request.
            If the search parameters differ from those mentioned above (for example, if FinCEN limits the search to
            a geographic location), the AML Compliance Person will structure our search accordingly.</p>
        <p><br>
        </p>
        <p>
            If the AML Compliance Person searches our records and does not find a matching account or transaction,
            then the AML Compliance Person will not reply to the 314(a) Request. We will maintain documentation that
            we have performed the required search.</p>
        <p><br>
        </p>
        <p>
            We will not disclose the fact that FinCEN has requested or obtained information from us, except to the
            extent necessary to comply with the information request. The AML Compliance Person will review, maintain
            and implement procedures to protect the security and confidentiality of requests from FinCEN similar to
            those procedures established to satisfy the requirements of Section 501 of the Gramm-Leach-Bliley Act
            regarding the protection of customers&rsquo; nonpublic information.</p>
        <p><br>
        </p>
        <p>We
            will direct any questions we have about the 314(a) Request to the requesting federal law enforcement
            agency as designated in the request.</p>
        <p><br>
        </p>
        <p>
            Unless otherwise stated in the 314(a) Request, we will not be required to treat the information request
            as continuing in nature, and we will not be required to treat the periodic 314(a) Requests as a
            government provided list of suspected terrorists for purposes of the customer identification and
            verification requirements.</p>
