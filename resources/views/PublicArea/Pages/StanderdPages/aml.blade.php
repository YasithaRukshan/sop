            <h3>
                &nbsp;&nbsp;&nbsp;&nbsp;c.Verifying Information</h3>
            <p>
                Based on the risk, and to the extent reasonable and practicable, we will ensure that we have a
                reasonable belief that we know the true identity of our customers by using risk-based procedures to
                verify and document the accuracy of the information we get about our customers. AML Compliance
                Person
                will analyze the information we obtain to determine whether the information is sufficient to form a
                reasonable belief that we know the true identity of the customer (<em>e.g.</em>, whether the
                information
                is logical or contains inconsistencies).</p>
            <p><br>
            </p>
            <p>
                We will verify customer identity through documentary means, non-documentary means or both. We will
                use
                documents to verify customer identity when appropriate documents are available. In light of the
                increased instances of identity fraud, we will supplement the use of documentary evidence by using
                the
                non-documentary means described below whenever necessary. We may also use non-documentary means, if
                we
                are still uncertain about whether we know the true identity of the customer. In verifying the
                information, we will consider whether the identifying information that we receive, such as the
                customer&rsquo;s name, street address, zip code, telephone number (if provided), date of birth and
                Social Security number, allow us to determine that we have a reasonable belief that we know the true
                identity of the customer (<em>e.g.</em>, whether the information is logical or contains
                inconsistencies).</p>
            <p><br>
            </p>
            <p>
                Appropriate documents for verifying the identity of customers include the following:</p>
            <p><br>
            </p>
            <ul class="ml-4">
                <li>
                    <p>
                         &bull;&nbsp;&nbsp;For an individual, an unexpired government-issued identification evidencing nationality or
                        residence and bearing a photograph or similar safeguard, such as a driver&rsquo;s license or
                        passport; and</p>
                </li>
            </ul>
            <p>
                <br></p>
            <ul class="ml-4">
                <li>
                    <p>
                         &bull;&nbsp;&nbsp; For a person other than an individual, documents showing the existence of the entity, such
                        as
                        certified articles of incorporation, a government-issued business license, a partnership
                        agreement or a trust instrument.</p>
                </li>
            </ul>
            <p>
                <br></p>
            <p>
                We understand that we are not required to take steps to determine whether the document that the
                customer
                has provided to us for identity verification has been validly issued and that we may rely on a
                government-issued identification as verification of a customer&rsquo;s identity. If, however, we
                note
                that the document shows some obvious form of fraud, we must consider that factor in determining
                whether
                we can form a reasonable belief that we know the customer&rsquo;s true identity.</p>
            <p><br>
            </p>
            <p>
                We will use the following non-documentary methods of verifying identity:</p>
            <p>
                <br></p>
            <ul class="ml-4">
                <li>
                    <p>
                         &bull;&nbsp;&nbsp;Independently verifying the customer&rsquo;s identity through the comparison of information
                        provided by the customer with information obtained from a consumer reporting agency, public
                        database or other source;</p>
                </li>
            </ul>
            <p>
                <br></p>
            <ul class="ml-4">
                <li>
                    <p>
