        <h3>
            4.&nbsp;&nbsp;Checking the Office of Foreign Assets Control Listings</h3>

        <p style="text-indent: 0in;margin-bottom: 0in;text-align: left;background: transparent;font-style: normal;">
            <br></p>
        <p>
            Before opening an account, and on an ongoing basis, the AML Compliance Person will check to ensure that
            a customer does not appear on the SDN list or is not engaging in transactions that are prohibited by the
            economic sanctions and embargoes administered and enforced by OFAC. Because the SDN list and listings of
            economic sanctions and embargoes are updated frequently, we will consult them on a regular basis and
            subscribe to receive any available updates when they occur. With respect to the SDN list, we may also
            access that list through various software programs to ensure speed and accuracy. <em>See also</em> <a
                href="http://apps.finra.org/RulesRegulation/OFAC/1/Default.aspx">FINRA&rsquo;s OFAC Search Tool</a>
            that screens names against the SDN list. AML Compliance Person will also review existing accounts
            against the SDN list and listings of current sanctions and embargoes when they are updated and he will
            document the review.</p>
        <p>
            <br></p>
        <p>
            If we determine that a customer is on the SDN list or is engaging in transactions that are prohibited by
            the economic sanctions and embargoes administered and enforced by OFAC, we will reject the transaction
            and/or block the customer&apos;s assets and file some blocked assets and/or rejected transaction form
            with OFAC within 10 days. We will also call the OFAC Hotline at (800) 540-6322 immediately.</p>
