                style="text-align: justify;background: transparent;font-size:16px;margin-left: 0.5in;text-indent: 0.5in;margin-bottom: 0in;">
                <br></p>
            <p>
                (i)obtain (<em>e.g.,</em> using a questionnaire) and consider information related to the foreign
                bank&rsquo;s AML program to assess the extent to which the foreign bank&rsquo;s correspondent
                account
