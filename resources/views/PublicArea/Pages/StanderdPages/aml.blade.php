                (2) the transaction is designed, whether through structuring or otherwise, to evade any requirements
                of
                the BSA regulations;</p>
            <p
               >
                (3) the transaction has no business or apparent lawful purpose or is not the sort in which the
                customer
                would normally be expected to engage, and after examining the background, possible purpose of the
                transaction and other facts, we know of no reasonable explanation for the transaction; or</p>
            <p
               >
                (4) the transaction involves the use of the Company to facilitate criminal activity.</p>
            <p><br>
            </p>
            <p>
                We will also file a SAR-SF and notify the appropriate law enforcement authority in situations
                involving
                violations that require immediate attention, such as terrorist financing or ongoing money laundering
                schemes. In addition, although we are not required to, we may contact that SEC in cases where a
                SAR-SF
                we have filed may require immediate attention by the SEC. <em>See</em> Section 11 for contact
                numbers.
                We also understand that, even if we notify a regulator of a violation, unless it is specifically
                covered
                by one of the exceptions in the SAR rule, we must file a SAR-SF reporting the violation.</p>
            <p>
                <br></p>
            <p>
                We may file a voluntary SAR-SF for any suspicious transaction that we believe is relevant to the
                possible violation of any law or regulation but that is not required to be reported by us under the
                SAR
                rule. It is our policy that all SAR-SFs will be reported regularly to the Board of Directors and
                appropriate senior management, with a clear reminder of the need to maintain the confidentiality of
                the
                SAR-SF.</p>
            <p>
                <br></p>
            <p>
                We will report suspicious transactions by completing a SAR-SF, and we will collect and maintain
                supporting documentation as required by the BSA regulations. We will file a SAR-SF no later than 30
                calendar days after the date of the initial detection of the facts that constitute a basis for
                filing a
                SAR-SF. If no suspect is identified on the date of initial detection, we may delay filing the SAR-SF
                for
                an additional 30 calendar days pending identification of a suspect, but in no case will the
                reporting be
                delayed more than 60 calendar days after the date of initial detection. The phrase &ldquo;initial
                detection&rdquo; does not mean the moment a transaction is highlighted for review. The 30-day (or
                60-day) period begins when an appropriate review is conducted and a determination is made that the
                transaction under review is &ldquo;suspicious&rdquo; within the meaning of the SAR requirements. A
                review must be initiated promptly upon identification of unusual activity that warrants
                investigation.
            </p>
            <p>
                <br></p>
            <p>
                We will retain copies of any SAR-SF filed and the original or business record equivalent of any
                supporting documentation for five years from the date of filing the SAR-SF. We will identify and
                maintain supporting documentation and make such information available to FinCEN, any other
                appropriate
                law enforcement agencies, federal or state securities regulators or SROs upon request.</p>
            <p>
                <br></p>
            <p>
                We will not notify any person involved in the transaction that the transaction has been reported,
                except
                as permitted by the BSA regulations. We understand that anyone who is subpoenaed or required to
                disclose
                a SAR-SF or the information contained in the SAR-SF will, except where disclosure is requested by
                FinCEN, the SEC, or another appropriate law enforcement or regulatory agency, or an SRO registered
                with
                the SEC, decline to produce the SAR-SF or to provide any information that would disclose that a
                SAR-SF
                was prepared or filed. We will notify FinCEN of any such request and our response.</p>
