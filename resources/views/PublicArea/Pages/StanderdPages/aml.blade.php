            <h3
               >
                9. &nbsp;&nbsp;Due Diligence and Enhanced Due Diligence Requirements for Private Banking Accounts/Senior Foreign
                Political Figures</h3>
            <p>
                We will review our accounts to determine whether we offer any private banking accounts and we will
                conduct due diligence on such accounts. This due diligence will include, at least, (1) ascertaining
                the
                identity of all nominal holders and holders of any beneficial ownership interest in the account
                (including information on those holders&apos; lines of business and sources of wealth); (2)
                ascertaining
                the source of funds deposited into the account; (3) ascertaining whether any such holder may be a
                senior
                foreign political figure; and (4) detecting and reporting, in accordance with applicable laws and
                regulations, any known or suspected money laundering, or use of the proceeds of foreign corruption.
            </p>
            <p>
                <br></p>
            <p>
                We will review public information, including information available in Internet databases, to
                determine
                whether any private banking account holders are senior foreign political figures. If we discover
                information indicating that a particular private banking account holder may be a senior foreign
                political figure, and upon taking additional reasonable steps to confirm this information, we
                determine
                that the individual is, in fact, a senior foreign political figure, we will conduct additional
                enhanced
                due diligence to detect and report transactions that may involve money laundering or the proceeds of
                foreign corruption.</p>
            <p>
                <br></p>
            <p>
                In so doing, we will consider the risks that the funds in the account may be the proceeds of foreign
                corruption by determining the purpose and use of the private banking account, location of the
                account
                holder(s), source of funds in the account, type of transactions conducted through the account and
                jurisdictions involved in such transactions. The degree of scrutiny we will apply will depend on
                various
                risk factors, including, but not limited to, whether the jurisdiction the senior foreign political
                figure is from is one in which current or former political figures have been implicated in
                corruption
                and the length of time that a former political figure was in office. Our enhanced due diligence
                might
                include, depending on the risk factors, probing the account holder&apos;s employment history,
                scrutinizing the account holder&apos;s source(s) of funds, and monitoring transactions to the extent
                necessary to detect and report proceeds of foreign corruption, and reviewing monies coming from
                government, government controlled or government enterprise accounts (beyond salary amounts).</p>
            <p>
                <br></p>
            <p>
                If we do not find information indicating that a private banking account holder is a senior foreign
                political figure, and the account holder states that he or she is not a senior foreign political
                figure,
                then we may make an assessment if a higher risk for money laundering, nevertheless, exists
                independent
                of the classification. If a higher risk is apparent, we will consider additional due diligence
                measures.
            </p>
            <p><br>
            </p>
            <p>
                In either case, if due diligence (or the required enhanced due diligence, if the account holder is a
                senior foreign political figure) cannot be performed adequately, we will, after consultation with
                the
                Company&apos;s AML Compliance Person and, as appropriate, not open the account, suspend the
                transaction
                activity, file a SAR-SF or close the account.</p>
