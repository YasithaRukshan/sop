        <p>
            We understand that the receipt of a grand jury subpoena concerning a customer does not in itself require
            that we file a Suspicious Activity Report (SAR-SF). When we receive a grand jury subpoena, we will
            conduct a risk assessment of the customer subject to the subpoena as well as review the customer&rsquo;s
            account activity. If we uncover suspicious activity during our risk assessment and review, we will
            elevate that customer&rsquo;s risk assessment and file a SAR-SF in accordance with the SAR-SF filing
            requirements. We understand that none of our officers, employees or agents may directly or indirectly
            disclose to the person who is the subject of the subpoena its existence, its contents or the information
            we used to respond to it. To maintain the confidentiality of any grand jury subpoena we receive, we will
            process and maintain the subpoena by the AML Compliance Person. If we file a SAR-SF after receiving a
            grand jury subpoena, the SAR-SF will not contain any reference to the receipt or existence of the
            subpoena. The SAR-SF will only contain detailed information about the facts and circumstances of the
            detected suspicious activity.</p>
