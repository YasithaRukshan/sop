            </ul>
            <p>
                <br></p>
            <ul class="ml-4">
                <li>
                    <p>
                         &bull;&nbsp;&nbsp;the type, purpose and anticipated activity of such correspondent account;</p>
                </li>
            </ul>
            <p>
                <br></p>
            <ul class="ml-4">
                <li>
