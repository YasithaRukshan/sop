            <h3
              >
              &nbsp;&nbsp; &nbsp;&nbsp; b.Currency Transaction Reports</h3>
            <p>
                A
                Company must file a currency transaction report (CTR) for each deposit, withdrawal, exchange of
                currency, or other payment or transfer by, through or to the Company that involves a transaction in
                currency of more than $10,000 or for multiple transactions in currency of more than $10,000 when a
                financial institution knows that the transactions are by or on behalf of the same person during any
                one
                business day, unless the transaction is subject to certain exemptions. &ldquo;Currency&rdquo; is
                defined
                as &ldquo;coin and paper money of the United States or of any other country&rdquo; that is
                &ldquo;customarily used and accepted as a medium of exchange in the country of issuance.&rdquo;
                Currency
                includes U.S. silver certificates, U.S. notes, Federal Reserve notes, and official foreign bank
                notes
                that are customarily used and accepted as a medium of exchange in a foreign country.</p>
            <p><br>
            </p>
            <p>
                Our Company prohibits transactions involving currency in excess of $10,000 If we discover such
                transactions have occurred, we will file with FinCEN CTRs for currency transactions that exceed
                $10,000.
                Also, we will treat multiple transactions involving currency as a single transaction for purposes of
                determining whether to file a CTR if they total more than $10,000 and are made by or on behalf of
                the
                same person during any one business day.</p>
            <p><br>
            </p>
            <h3
              >
              &nbsp;&nbsp; &nbsp;&nbsp;c.Currency and Monetary Instrument Transportation Reports</h3>
            <p ><em>A
                    currency
                    and</em><span >&nbsp;monetary instrument transportation report (CMIR)
                    must be filed whenever more than $10,000 in currency or other monetary instruments is physically
                    transported, mailed or shipped into or from the United States. A CMIR also must be filed
                    whenever a
                    person receives more than $10,000 in currency or other monetary instruments that has been
                    physically
                    transported, mailed or shipped from outside the United States and a CMIR has not already been
                    filed
                    with respect to the currency or other monetary instruments received. A CMIR is not required to
                    be
                    filed by a securities broker-dealer mailing or shipping currency or other monetary instruments
                    through the postal service or by common carrier. &ldquo;Monetary instruments&rdquo; include the
                    following: currency (defined above); traveler&apos;s checks in any form; all negotiable
                    instruments
                    (including personal and business checks, official bank checks, cashier&apos;s checks,
                    third-party
                    checks, promissory notes and money orders) that are either in bearer form, endorsed without
                    restriction, made out to a fictitious payee or otherwise in such form that title passes upon
                    delivery; incomplete negotiable instruments that are signed but omit the payee&apos;s name; and
                    securities or stock in bearer form or otherwise in such form that title passes upon
                    delivery.</span>
            </p>
            <p><br></p>
            <p>
                Our Company prohibits both the receipt of currency or other monetary instruments that have been
                transported, mailed or shipped to us from outside of the United States, and the physical
                transportation,
                mailing or shipment of currency or other monetary instruments by any means other than through the
                postal
                service or by common carrier. We will file a CMIR with the Commissioner of Customs if we discover
                that
                we have received or caused or attempted to receive from outside of the U.S. currency or other
                monetary
                instruments in an aggregate amount exceeding $10,000 at one time (on one calendar day or, if for the
                purposes of evading reporting requirements, on one or more days). We will also file a CMIR if we
                discover that we have physically transported, mailed or shipped or caused or attempted to physically
                transport, mail or ship by any means other than through the postal service or by common carrier
                currency
                or other monetary instruments of more than $10,000 at one time (on one calendar day or, if for the
                purpose of evading the reporting requirements, on one or more days).</p>
