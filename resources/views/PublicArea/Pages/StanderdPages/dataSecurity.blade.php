            <h3 class="row clearfix justify-content-center">DATA SECURITY POLICY</h3>
            <h3 class="mt-4">Organizational Controls</h3>
            <p>
                Solar Oil Enterprises Limited (the &ldquo;Company&rdquo;) personnel are expected to be competent,
                thorough, helpful, and courteous stewards of customer information that is stored on the Company&rsquo;s
                products and in the Company&rsquo;s data centers. The Company has established a number of measures to
                ensure that customers and their data are treated properly.</p>


            <h3 class="mt-4">Privacy and Control Mechanisms</h3>
            <p>
                The Company only uses the information provided by our customers to deliver the products and services
                purchased. All customer data is managed in compliance with our Privacy Policy. In addition, some
                products and all US Data Centers are audited to provide an independent validation of our policies and
                procedures around securing customer data.</p>
            <p>
                The Company complies with any portion of HIPAA or the HITECH Act that are directly applicable to the
                Company. In particular, the Company Platform safeguards replicated data in such a way as to satisfy
                HIPAA&rsquo;s Security Rule. Customers wishing to establish a Business Associate relationship with the
                Company per 45 CFR 164.502(e) and 164.504(e) should request a Business Associate Agreement from the
                Company. The Business Associate Agreement defines commitments that the Company will make to maintain
                HIPAA and HITECH compliance as required</p>


            <h3 class="mt-4">Company Employees</h3>
            <p>
                All employees are required to accept and acknowledge in writing the Company&rsquo;s policies for
                nondisclosure and protection of the Company and third-party confidential information, including
                acceptable use of confidential information. In the course of assisting customers with their technology
                solutions, the Company support technicians understand that they may come into contact with customer
                communications and/or customer data and they must keep this information confidential.</p>

            <h3 class="mt-4">Trainings</h3>

            <p>
                Technicians who support the Company products are prepared in a variety of ways. New tier 1 technicians
                receive class time training with tier 2 technicians and the support management team. New support
                technicians also spend a period of time as an understudy to an established technician for each product
                in which they intend to become certified. Product knowledge is tested and established through formal
                online training and all technicians are expected to meet a pre-defined standard before supporting
                customers directly.</p>
            <p>
                All Company support technicians receive ongoing training in product-specific training sessions.</p>
            <p>
                When an employee or contractor leaves the Company, a formal process is in place to immediately revoke
                physical and network access to the Company&rsquo;s facilities and resources.</p>


