            <p
                style="margin-bottom: 0.19in;line-height: 100%;text-align: left;background: transparent;margin-top: 0.19in;">

                <h3 class="mt-4">Storage Facility Standards</h3>
                <p>
                    The Company leases space in a number of data centers worldwide. Each Company data center is equipped
                    with the following:</p>
                <ul class="ml-4">
                    <li>
                        <p>
                             &bull;&nbsp;&nbsp;Controlled access systems requiring key-card authentication</p>
                    </li>
                    <li>
                        <p>
                             &bull;&nbsp;&nbsp;Video-monitored access points</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Intrusion
                            alarms</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Locking
                            cabinets</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Climate
                            control systems</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Waterless
                            fire-suppressant systems</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Redundant
                            power (generator backup, UPS, no single point of failure)</p>
                    </li>
                    <li>
                        <p>  &bull;&nbsp;&nbsp;Redundant
                            Internet connectivity</p>
                    </li>
                    <li>
                        <p> &bull;&nbsp;&nbsp;ISO
                            and/or SOC II certified</p>
                    </li>
                </ul>

                <h3 class="mt-4">Data Location</h3>
                <p>
                    Knowing the geographic location of their data is important for customers operating in regulated
                    industries or in countries with data protection laws. The Company understands that some customers
                    must
                    maintain their data in a specific geographic location, such as within the European Union or within
                    countries that are members of the Asia-Pacific Economic Cooperation (APEC) forum.</p>
                <p>
                    To that end, the Company maintains a network of Platform-scale data centers by geographic location
                    around the globe, and verifies that each meets defined security requirements. However, not all
                    Company
                    products are deployed in all regions. To determine where data for a particular Company product is
                    stored, please refer to the product-specific security document.</p>

                <h3 class="mt-4">Redundancy</h3>
                <p>
                    Data in the Company Platform is stored in a proprietary storage system developed and managed by the
                    Company. This system maintains two copies of customer data to provide redundancy. In the United
                    States,
                    the two copies are stored in separate data center locations. Outside of the United States, the two
                    copies are stored within the same location on separate storage systems.</p>

                <h3 class="mt-4">Platform Security</h3>
                <p>
                    The Company uses a defense-in-depth strategy and proprietary hardened software and operating systems
                    to
                    protect data and services. The Company conducts regular inspections to ensure the security of its
                    systems.</p>


                <h3 class="mt-4">Data Privacy</h3>

                <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;1. Your Data</h3>
                <p>
                    Data stored in the Company Platform is our customers&rsquo; data and we protect their right to make
                    decisions about that data and we are transparent about what happens to that data. With the Company
                    Platform, you are the owner of your customer data.</p>
                <p>
                    Customer data is defined as all data, including text, sound, video, or image files and software,
                    that
                    you provide to the Company, or is provided on your behalf.</p>
                <p>
                    The Company will use your customer data only to provide the services we have agreed upon, and for
                    purposes that are compatible with providing those services.</p>
                <p>
                    You can access your customer data at any time and for any reason without assistance from the
                    Company. We
                    restrict access to it to the Company personnel and subcontractors. We provide simple, transparent
                    data-use policies.</p>



                <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;We do not use customer data for advertising</h3>
                <p>
                    Except as set forth below, the Company does not share customer data with our advertiser-supported
                    services, nor do we mine it for marketing or advertising.</p>
                <p>
                    In addition to providing the service and day-to-day operations, the Company may use your data for
                    the
                    following:</p>
                <ul>
                    <li>
                        <p>
                            Troubleshooting aimed at preventing, detecting, and repairing problems affecting the
                            operation
                            of services</p>
                    </li>
                    <li>
                        <p>
                            Ongoing
                            improvement of features, such as those that improve the reliability of our services, or
                            involve
                            the detection of, and protection against, threats to the services or customer data (such as
                            malware or spam)</p>
                    </li>
                    <li>
                        <p>
                            Providing personalized customer experiences</p>
                    </li>
                    <li>
                        <p style="text-align: justify;background: transparent;margin-bottom: 0.19in;line-height: 100%;">
                            Contacting you about new products and services</p>
                    </li>
                </ul>
                <p>
                    Furthermore, the Company Platform uses systems that are kept logically separate from internal
                    systems
                    run by the Company.</p>

                <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;We use logical isolation to segregate each customer&rsquo;s
                    data from that of
                    others&nbsp;</h3>
                <p>
                    The Company Platform services are multi-tenant services, meaning that your data, deployments, and
                    virtual machines may be stored on the same physical hardware as that of other customers. When data
                    from
                    many customers is stored at a shared physical location, the Company logically segregates storage and
                    processing for different customers through specialized technology engineered specifically for that
                    purpose.</p>
                <p>
                    The Company takes strong measures to protect customer data from inappropriate use or loss and to
                    prevent
                    customers from gaining access to one another&rsquo;s data.</p>


                <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;We provide simple, transparent data-use policies and get
                    independent audits</h3>
                <p>
                    The Company provides you with details of our data protection policies and practices in clear,
                    straightforward language in the Privacy Policy.</p>

                <h3 class="mt-4">&nbsp;&nbsp;&nbsp;&nbsp;Our subcontractors are contractually obligated to meet our
                    privacy
                    requirements&nbsp;</h3>
                <p>
                    The Company may hire other companies to provide limited services, such as data colocation services.
                    We
                    provide customer data as required to deliver the services we have retained them to provide.
                    Subcontractors are prohibited from using customer data for any other purpose, and they are required
                    to
                    maintain the confidentiality of our customers&rsquo; information.</p>
                <ul>
                    <li>
                        <p>
                            Subcontractors who handle customer data in the Company Platform services must enter into
                            additional agreements with the Company that subject them to data protection terms.</p>
                    </li>
                    <li>
                        <p style="text-align: justify;background: transparent;margin-bottom: 0.19in;line-height: 100%;">
                            Subcontractors who handle the Company Platform customer data in their own facilities are
                            required to set up and follow privacy standards equivalent to our own.</p>
                    </li>
                </ul>


                <h3 class="mt-4">Control of your Data</h3>


                <h3 class="mt-4">You control access to your customer data&nbsp;</h3>


                <h3 class="mt-4">Access by Company personnel.&nbsp;</h3>
                <p>
                    The Company&rsquo;s personnel are granted access only when necessary under management oversight. The
                    Company&rsquo;s personnel will use customer data only for purposes compatible with providing you the
                    services, which can include customer support and troubleshooting services.</p>

                <h3 class="mt-4">Access by subcontractors&nbsp;</h3>
                <p>
                    The Company may hire other companies to provide limited services. Subcontractors can access customer
                    data only to deliver the services we have hired them to provide. Subcontractors are prohibited from
                    using customer data for any other purpose, and are required to maintain the confidentiality of our
                    customers&rsquo; information.</p>

                <h3 class="mt-4">Limits to access.&nbsp;</h3>
                <p>
                    The operational processes and controls that govern access to and use of customer data in the Company
                    Platform are regularly verified. The Company regularly performs sample audits to attest that access
                    is
                    only for legitimate business purposes. Strong controls and authentication help limit access to
                    customer
                    data to authorized personnel only. When access is granted, whether to the Company personnel or our
                    subcontractors, it is carefully controlled and logged, and revoked as soon as it is no longer
                    needed.
                </p>
                <p
                    style="margin-bottom: 0.19in;line-height: 100%;text-align: left;background: transparent;margin-top: 0.19in;">
                    <strong>Government and law enforcement requests.</strong></p>
                <h3 class="mt-4">Government and law enforcement requests.&nbsp;</h3>
                <p>
                    The Company imposes carefully defined requirements around government and law enforcement requests
                    for
                    customer data. We will not disclose data hosted in the Company Platform to a government agency
                    except as
                    you direct or where required by law. When we receive a government or law enforcement request for
                    customer data, we attempt to redirect the third-party to obtain the requested data from our
                    customer.
                </p>

                <h3 class="mt-4">You control your customer data if you leave the service&nbsp;</h3>
                <p>
                    The Company follows strict standards and specific processes for removing customer data from all
                    systems
                    under our control.</p>

                <h3 class="mt-4">Data portability&nbsp;</h3>
                <p>
                    You can retrieve a copy of your customer data at any time and for any reason without any assistance
                    or
                    notification required from the Company.</p>

                <h3 class="mt-4">Data retention&nbsp;</h3>
                <ul class="ml-4">
                    <li>
                        <p> &bull;&nbsp;&nbsp;
                            If you, the customer, terminate your subscription or it expires (except for free trials),
                            the
                            Company will store your customer data in a limited-function account for 30 days (the
                            retention
                            period) to give you time to export the data or renew your subscription. During this period,
                            the
                            Company provides multiple notices, so you will be amply forewarned of the upcoming deletion
                            of
                            data.</p>
                    </li>

                    <li>
                        <p class="mb-3"> &bull;&nbsp;&nbsp;
                            After this 30-day retention period, the Company will disable the account and may delete all
                            customer data at its discretion, including any cached or backup copies.</p>
                    </li>
                </ul>
                <p>
                    In the multitenant environments of the Company Platform services, we take careful measures to
                    logically
                    separate customer data to help prevent one customer&rsquo;s data from leaking into the data of
                    another
                    customer, as well as to help block any customer from accessing another customer&rsquo;s deleted
                    data.
                </p>

                <h3 class="mt-4">Data deletion on physical storage devices&nbsp;</h3>
                <ul>
                    <li>
                        <p>
                            When a disk drive used for storage in the Company Platform suffers a hardware failure, it is
                            securely erased or destroyed before the Company returns it to the manufacturer for
                            replacement
                            or repair. All of the data on the drive is completely overwritten to ensure that the data
                            cannot
                            be recovered by any means.</p>
                    </li>
                </ul>

                <h3 class="mt-4">You have options to control the security of your customer data&nbsp;</h3>
                <p>
                    The Company Platform uses encryption to safeguard your data and help you maintain control over it.
                </p>
                <p>
                    When customer data moves over a network, the Company Platform uses industry- standard secure
                    transport
                    protocols between user devices and Company data centers, as well as within the data centers
                    themselves.
                </p>
                <p>
                    The Company Platform uses industry-standard encryption for data at rest in transit.</p>

                <h3 class="mt-4">How we respond to government requests.&nbsp;</h3>
                <p>
                    When governments or law enforcement make a lawful request for customer data from the Company, we are
                    committed to transparency and limit what we disclose. Because the Company believes that customers
                    should
                    control their own data, we will not disclose data hosted in the Company Platform to a government or
                    law
                    enforcement agency except as you direct or where required by law.</p>

                <h3 class="mt-4">We do not offer direct access to customer data.&nbsp;</h3>
                <p>
                    We believe that you should control your own data. The Company does not give any third-party
                    (including
                    law enforcement, other government entity, or civil litigant) direct or unfettered access to customer
                    data except as you direct, or as required by law.</p>

                <h3 class="mt-4">We redirect law enforcement and other third-party requests to the customer.&nbsp;</h3>
                <p>
                    When we receive a government or law enforcement request for customer data, we always attempt to
                    redirect
                    the third third-party to obtain the requested data from our customer.</p>
                <p>
                    For valid requests that we are not able to redirect to the customer, we disclose information only
                    when
                    we are legally compelled to do so, and we always make sure that we provide only the data specified
                    in
                    the legal order.</p>
                <p>
                    In either case, requests may require the release of the customer&rsquo;s basic contact information.
                </p>

                <h3 class="mt-4">We do not give access to platform encryption keys&nbsp;</h3>
                <p>
                    We do not provide any government with our encryption keys or the ability to break our encryption.
                </p>

